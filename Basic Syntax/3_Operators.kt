//    Arithmetic Operators : + - * / %

fun main() 
{  
    val num1 = 10
    val num2 = 20
    
    println("Addition is : ${num1 + num2}")
    println("Subtraction is : ${num1 - num2}")
    println("Multiplication is : ${num1 * num2}")
    println("Division is : ${num1 / num2}")
    println("Modulus is : ${num1 % num2}")
}



//    Comparison Operators : < ,>, <= ,>=, ==

fun main() 
{  
    val a = 10
    val b = 20
    
    println(a<b)
    println(a<=b)
    println(a>b)
    println(a>=b)
    println(a==b)
    
}


//    Logical Operators :  &&, ||, !=

fun main() 
{  
    var a = false
    var b = true
    
   println(a && b)
   println(a || b)
   println(!a)
}

